#include<iostream>
using namespace std;

int main()
{
    int a=4,b=6;

    //Bitwise Operator

    //AND
    cout<<"a & b : "<<(a&b); cout<<"\n";
    //OR
    cout<<"a | b : "<<(a|b); cout<<"\n";
    //XOR
    cout<<"a ^ b : "<<(a^b); cout<<"\n";
    //NOT
    cout<<"~a : "<<(~a); cout<<"\n";

    //Left Shift Operator
    cout<<"left shift operaoro : "<<(19<<1)<<"\n";
    cout<<"left shift operaoro : "<<(19<<2)<<"\n";

     //Right Shift Operator
    cout<<"Right shift operaoro : "<<(17>>1)<<"\n";
    cout<<"Right shift operaoro : "<<(17>>2)<<"\n";



}